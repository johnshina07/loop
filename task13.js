
let number = 7;

// Conditional Statements
if (number > 0) {
    console.log("The number is positive.");
} else if (number < 0) {
    console.log("The number is negative.");
} else {
    console.log("The number is zero.");
}



// 1. For Loop
console.log("For Loop:");
for (let i = 1; i <= 5; i++) {
    console.log("i = " + i);
}

// 2. While Loop
console.log("While Loop:");
let j = 1;
while (j <= 5) {
    console.log("j = " + j);
    j++;
}

// 3. Do-While Loop
console.log("Do-While Loop:");
let k = 1;
do {
    console.log("k = " + k);
    k++;
} while (k <= 5);